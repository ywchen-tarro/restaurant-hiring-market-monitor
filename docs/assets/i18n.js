  function applyTranslations() {
    document.documentElement.lang = currentLang === 'zh' ? 'zh' : 'en';

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.dataset.i18n;
      const val = window.I18N.t(key);
      if (el.dataset.i18nHtml === 'true') {
        el.innerHTML = val;
      } else {
        el.textContent = val;
      }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      el.placeholder = window.I18N.t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = window.I18N.t(el.dataset.i18nTitle);
    });

    // <title> tag
    document.title = window.I18N.t('siteTitle');

    // language toggle button label
    const langBtn = document.getElementById('langToggle');
    if (langBtn) langBtn.textContent = window.I18N.t('langLabel');
  }

